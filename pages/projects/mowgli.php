<div class="heading">libmowgli</div>

	<div class="subheading">A useful collection of routines for programming. Performance and usability-oriented extensions to C.</div>

	<br /><br />

	<div id="features">
	<strong>libmowgli Contains:</strong>
	<ul>
	<li>mowgli_alloc: A safe wrapper around malloc/free.</li>
	<li>mowgli_argstack: Safe serialization of valists.</li>
	<li>mowgli_assert: Various assertion routines that can be used.</li>
	<li>mowgli_bitvector: Bitmasks with an unlimited level of precision.</li>
	<li>mowgli_dictionary: A keyword-backed definition hashtable class.</li>
	<li>mowgli_error_backtrace: Provide feedback to users on what caused the error they are recieving.</li>
	<li>mowgli_exception: Assertions with user feedback.</li>
	<li>mowgli_formatter: A simple token formatter which is sometimes useful.</li>
	<li>mowgli_global_storage: A simple global storage library.</li>
	<li>mowgli_hash: A portable implementation of the FNV-1 hash.</li>
	<li>mowgli_heap: An optimistic heap-based memory allocator.</li>
	<li>mowgli_hook: A simple hooks API you can use for your application, which allows for hooks to provide both application data and user data.</li>
	<li>mowgli_ioevent: Portable I/O completion ports for many OSes.</li>
	<li>mowgli_list: A high performance linked lists implementation with O(1) scalability for most common operations.</li>
	<li>mowgli_logger: An internal class for handling logging of exceptions.</li>
	<li>mowgli_memorypool: A class which allows for memory pooling.</li>
	<li>mowgli_module: A wrapper around dlopen(3) and dlsym(3).</li>
	<li>mowgli_object: A simple class which provides reference counted pointers and polymorphism of structs.</li>
	<li>mowgli_object_class: Classing and subclassing for objects.</li>
	<li>mowgli_object_metadata: Metadata for objects.</li>
	<li>mowgli_object_messaging: Messaging and signalling for objects.</li>
	<li>mowgli_queue: A simple class which implements double-ended queues.</li>
	<li>mowgli_random: A high performance psuedo-random number generator.</li>
	<li>mowgli_signal: A wrapper for sigaction(2).</li>
	<li>mowgli_spinlock: Portable spinlocks.</li>
	</ul>
	</div>

	<br />

	<strong>Download the current development branch (requires Git):</strong><br />
	<span class="cli">$ git clone git://git.atheme.org/libmowgli.git</span>

	<br /><br />

	<strong>Installation is fairly typical:</strong><br />
	<span class="cli">$ ./configure</span><br />
	<span class="cli">$ make</span><br />
	<span class="cli">$ sudo make install</span><br />

